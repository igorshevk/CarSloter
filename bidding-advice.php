                <strong>1. Activate browser notifications</strong><br>
                With an overload of e-mails already piling up in your inbox, we needed a way to inform you quicker and more effectively on what is happening in our auctions. For some auction events, you want to be able to act immediately, such as when you are overbid or when an x-Time is starting. We have therefore introduced <a href="#"><span style="text-decoration: underline; color: #008c95;">browser notifications</span></a>.<br>
                <br>
                Browser notifications are messages in your browser that appear in the bottom right corner of your screen. These messages will also appear outside of the CarSloter environment, when you are not logged in or when you are surfing on other websites.<br>
                <br>
                We currently have 3 events for which you can activate browser notifications:<br>
                &nbsp; &nbsp;• When you have been overbid on a car you are bidding on;&nbsp;<br>
                &nbsp; &nbsp;• When the x-Time phase is starting for a car you are bidding on or following;&nbsp;<br>
                &nbsp; &nbsp;• When a car you are bidding on or following has reached the Estimated Price.<br>
                <br>
                <strong>2. Guarenteed assignment&nbsp;</strong></p>
                • <a href="#"><span style="text-decoration: underline; color: #008c95;">100% assignment:</span></a> If your bid is high enough to win an auction, it’s high enough for the seller too. They will assign the vehicle to you after the auction concludes.<br>
