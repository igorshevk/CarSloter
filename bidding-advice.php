                <h3 style="text-align: justify;">Bidding process</h3>
                <strong><br>
                STEP 1:</strong> Log in and go to the car detail page of the vehicle you want to place a bid on.<br>
                <br>
                <strong>STEP 2: </strong>Enter your bid: Type in the amount or use the + / - button. You cannot place a bid lower than the starting price or the current highest bid.&nbsp;You will immediately see the total price you will need to pay at that bid level, inclusive fees and taxes.<br>
                <br>
                <strong>STEP 3:</strong> Choose your bidding method:<br>
                • Bid agent: You enter the maximum amount that you are willing to bid for a vehicle. However, this is not the amount that will appear on the website. The bid agent will only bid the lowest amount possible to surpass the previous bid. And, it will keep doing this until your maximum amount is reached.<br>
                <br>
                • Maximum bid or direct bid You place a bid of € X, and that bid is immediately shown on the website. For your convenience, we recommend to place maximum bids immediately from the „MY BIDS” tab, visible only after login.<br>
                <br>
                <strong>STEP 4: </strong>Submit your bid by clicking the „Place bid” button<br>
                <span style="text-decoration: underline;">Remember:</span> all bids are binding and cannot be cancelled or removed from the website.<br>
                <br>
                <strong>STEP 5:</strong> Place your best and final bid with Ultimo.<br>
                <br>
                <strong>STEP 6:</strong> Follow up on bids using browser notifications or notifications on your smartphone via our mobile app.<br>
                <p>&nbsp;</p>
                
                <p style="text-align: justify;">
                
                <br>
                If you hold the highest bid at the end of an auction, you will receive a notification by e-mail and/or in your browser. Apart from notifying you that you have won the auction, this notification will contain a one-time invitation to put in your best and final bid. By clicking on the link, you will be directed to the Car Detail page of the relevant vehicle.</p>
                <p style="text-align: justify;">The interface is exactly the same as during the auction. Type in your final bid and click the „Place bid” button. After clicking this button, the auction will be closed. It is important to note that every bid is binding, also a bid submitted during the Ultimo phase.</p>
